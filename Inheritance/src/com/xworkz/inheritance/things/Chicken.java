package com.xworkz.inheritance.things;

public class Chicken {
	
	public String name;
	public String color;
	public String types;
	public int price;
	public int quantity;
	public String size;
	public String taste;
	
	
	public Chicken()
	{
		System.out.println("Create chicken using no-const");
	}
	
	public void fry()
	{
		System.out.println("running fry in chicken");
	}
	
	public void fire()
	{
		System.out.println("Running fire in chicken");
	}
	
	public void masala()
	{
		System.out.println("running masala in chicken");
	}
	
	public void spicy()
	{
		System.out.println("Running spicy in chicken");
	}
	
	public void show()
	{
		System.out.println("Running show in chicken");
	}
}
